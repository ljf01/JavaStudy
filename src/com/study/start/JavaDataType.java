public class JavaDataType {
    public static void main(String[] args)
    {
        int i = 5;
        long m = 12_341_231_231_232L;
        System.out.println(m);
        System.out.println(i);
        /*数据类型：
        * 整数类型：
        * byte
        * int
        * short
        * long
        * */

        /*
        * 小数类型
        * float
        * double
        * */
        float f = 12.44f;
        double d = 12.665_55;
        System.out.println(f);
        System.out.println(d);}
}
